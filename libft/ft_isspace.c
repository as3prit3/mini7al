#include "libft.h"

int	ft_isspace(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
			i++;
		else
			return (0);
	}
	return (1);
}

int	ft_space(char c)
{
	return (c == ' ' || (c >= 9 && c <= 13));
}
